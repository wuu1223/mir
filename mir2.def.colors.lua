function colors.get(id, is4b)
	local r, g, b = nil

	if id and id >= 0 and id <= 255 then
		b = colors[id + 1][3]
		g = colors[id + 1][2]
		r = colors[id + 1][1]
	else
		b = 255
		g = 255
		r = 255
	end

	if is4b then
		return cc.c4b(r, g, b, 255)
	end

	return cc.c3b(r, g, b)
end

function colors.get4f(id)
	local clr = colors.get(id)

	return cc.c4f(clr.r / 255, clr.g / 255, clr.b / 255, 1)
end

