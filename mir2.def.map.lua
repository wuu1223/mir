function map.getTransferPoint(srcMap, dstMap)
	local key = srcMap .. "-" .. dstMap
	local nextMap, points = nil

	for k, v in pairs(map.path) do
		if k == key then
			nextMap = v[1]

			break
		end
	end

	if nextMap then
		key = srcMap .. "-" .. nextMap
	end

	for k, v in pairs(map.transferPoints) do
		if k == key then
			points = v

			break
		end
	end

	return points
end

function map.isBanPoint(x, y, mapid)
	if map.banPoints[mapid .. ""] and map.banPoints[mapid .. ""][x * 10000 + y] then
		return true
	end

	return false
end

function map.getMinimapID(mapid)
	mapid = string.lower(mapid)

	if map.noMinimap[mapid] then
		return
	end

	return map.mmapReplace[mapid] or mapid
end

