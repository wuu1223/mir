local function getNpcFrame(appr)
	if appr == 23 then
		return def.MA34, true
	elseif checkExist(appr, 24, 25, 27, 32) then
		return def.MA35, true
	elseif checkIn(appr, 33, 34) or appr == 130 or appr == 131 then
		return def.MA33, false
	elseif checkIn(appr, 35, 41) or checkIn(appr, 48, 50) or appr == 52 or appr == 53 then
		return def.MA41, false
	elseif checkIn(appr, 35, 41) then
		return def.MA46, false
	elseif appr == 54 then
		return def.MA34, false
	elseif checkIn(appr, 70, 75) or checkIn(appr, 80, 85) then
		return def.MA52, false
	elseif checkIn(appr, 90, 92) then
		return def.MA52, false
	elseif appr == 94 then
		return def.MA57, false
	elseif checkIn(appr, 95, 97) then
		return def.MA55, false
	elseif checkIn(appr, 98, 99) then
		return def.MA57, true
	elseif appr == 100 then
		return def.MA57, true
	elseif appr == 101 then
		return def.MA58, false
	elseif checkIn(appr, 102, 106) then
		return def.MA60, false
	elseif appr == 107 then
		return def.MA65, true
	elseif appr == 108 then
		return def.MA72, true
	elseif checkIn(appr, 109, 119) then
		return def.MA75, false
	elseif appr == 146 then
		return def.MA41, true
	elseif appr == 153 then
		return def.MA91, true
	elseif appr == 169 then
		return def.MA94, true
	end

	return def.MA33, true
end

function def.getFrame(race, appr)
	if race == 50 then
		return getNpcFrame(appr)
	else
		local data = monConfigWithAppr[appr]

		if data then
			return data
		end

		data = monConfig[race]

		if data then
			if data.check_appr then
				return data[appr] or data.default
			else
				return data
			end
		end
	end
end

